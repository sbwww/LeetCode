// @lc code=start
class Solution {
  public:
    vector<int> nums;
    vector<int> original;
    Solution(vector<int> &nums) {
        this->nums = nums;
        this->original = nums;
    }

    vector<int> reset() { return original; }

    vector<int> shuffle() {
        for (int i = 0; i < nums.size(); ++i) {
            int j = i + rand() % (nums.size() - i);
            swap(nums[i], nums[j]);
        }
        return nums;
    }
};

